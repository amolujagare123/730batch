public class Increntdemo {

    public static void main(String[] args) {

        int i = 10;

        System.out.println(i); // 10
        System.out.println(i++);  // 10
        // i=11
        System.out.println(++i); // 12
        System.out.println(i);//12

        /*
i++ :- here first the statement is executed the value is incremented

++i :- here first the value is incremented then the statement is executed*/



    }






}




